package items;

/**
 * The ItemType enum represents different types of items in the game.
 */
public enum ItemType {
    FOOD,       // Represents food items
    WEAPON,     // Represents weapon items
    ARMOR,      // Represents armor items
    POTION,     // Represents potion items
    QUEST,      // Represents quest items
    MISC,       // Represents miscellaneous items
    DEFAULT,    // Represents default items
    FORESTKEY,  // Represents a key for forest areas
    WIN;        // Represents the winning item
}
